           <p><a href="http://skinnyties.com/" target="_blank"> <img src="images/skinny-ties-website.png"/></a></p>
            <p>During early internet days, NarrowTies.com, which later evolved into SkinnyTies.com, was one of the first companies to sell traditional neckware online.</p>
          </article>
          <div class="col-md-6">
            <p>SkinnyTies decided to use RWD to reinvent their brand's identity and make their website future-friendly regardless of platform. This decision resulted a huge improvement for the brand.</p>
            <p><strong>Results:</strong></p>
            <ul>
                <li>42% increase in revenue from all devices</li>
                <li>13% increase in conversion rate</li>
                <li>377% increase in iPhone revenue</li>
                <li>71% increase in iPhone conversion</li>
                <li>23% decrease in bounce Rate</li>
                <li>44% increase in visit duration</li>
            </ul>
