                </a>
              </h4>
            </div>
            <div id="collapse24" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading24">
              <div class="panel-body">
        		<p>You can't force a user to hold on one orientation. So make sure your website looks good in any orientation. Only in immersive experiences can you offer functionality suited to one orientation or the other.</p>
              </div>
            </div>
