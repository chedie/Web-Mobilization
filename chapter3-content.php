                <a data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="true" aria-controls="collapseTwo">2. Define your objective now and for the future.
                </a>
              </h4>
            </div>
            <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
              <div class="panel-body">
        		<p>Whether you already own a website or you are to start one from scratch, always think ahead.</p>
                <p>You will need to define your immediate mobile website needs. Remember that technology evolves fast. It is highly recommended to build something that can be evolved in a year or two. This will make life easier rather than do a complete redesign overhaul soon.</p>
