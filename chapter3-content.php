            <div class="panel-heading" role="tab" id="heading9">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse9" aria-expanded="true" aria-controls="collapse9">9. Keep your pages as small as possible.
                </a>
              </h4>
            </div>
            <div id="collapse9" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading9">
              <div class="panel-body">
              	<article class="col-md-6">
                    <p>You can't have a mobile website load slowly because of tons of files loading in the background. Yes, we are living in the days of broadband internet. But keep in mind that not all of your visitors is constantly at home or close to their wi-fi points.</p> 
                    <p>Implement performance best practices do decrease file sizes. Example of these methods are to minify CSS and JavaScript files, or to optimize images. If this sounds too technical for you, consult a developer for help.</p>
                    <p>To check if your website loads fast, go to Google's PageSpeed Insights page (<a href="http://bit.ly/18oQqhi" target="_blank">http://bit.ly/18oQqhi</a>).</p>
              	</article>
                <article class="col-md-6">
                	<img src="images/icon-pagespeed-insights.jpg" />
                    <small>Google PageSpeed Insights: <a href="http://bit.ly/18oQqhi" target="_blank">http://bit.ly/18oQqhi</a></small>
                	<p>This page helps discover if your website has issues for slow loading time. You will also be provided with solutions to fix each problem that comes out.</p>
                </article>
              </div>
            </div>
          </div>     
          
          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading10">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse10" aria-expanded="true" aria-controls="collapse10">10. Provide an easy scroll experience.
                </a>
              </h4>
            </div>
            <div id="collapse10" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading10">
              <div class="panel-body">
        		<p>Never set up your mobile site to have your visitors "pinch and zoom." This is a lot of hassle and will make your visitors leave your site. Use checkable boxes, scrolling menu bars, large buttons, etc. that will make user-experience smooth and fluid.</p>
              </div>
            </div>
          </div>     
          
          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading11">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse11" aria-expanded="true" aria-controls="collapse11">11. If designing for tablet, consider ideal touch regions.
                </a>
              </h4>
            </div>
            <div id="collapse11" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading11">
              <div class="panel-body">
        		<p>For example, consider how a user holds a tablet. When holding it with both hands, the most accessible touch target areas of your site are the bottom left and right. These regions can be reached by the user's thumbs.</p>
                <p>If you have interactive buttons near the top of your screen, the user will have to hold the device in one hand, then with the other reach and touch.</p> 
                <p>You don't have to be strict about touch regions but it is something to keep in mind for immersive tablet experiences.</p>
              </div>
            </div>
          </div>     
          
          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading12">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse12" aria-expanded="true" aria-controls="collapse12">12. Enlarge touch targets (inclusive of links and input fields).
                </a>
              </h4>
            </div>
            <div id="collapse12" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading12">
              <div class="panel-body">
        		<p>Keep links from being to close together to make accidental visits to the wrong form field page. Farther than a thumbs width apart will ideally work.</p>
              </div>
            </div>
          </div>     
          
          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading13">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse13" aria-expanded="true" aria-controls="collapse13">13. Ensure there are no overlapping layout issues.
                </a>
              </h4>
            </div>
            <div id="collapse13" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading13">
              <div class="panel-body">
        		<p>This can happen due to incorrect positioned elements, increased font-size, wrapping, etc. Don't make your user be frustrated over content he couldn't read because it is covered by another element.</p>
              </div>
            </div>
          </div>     
      
          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading14">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse14" aria-expanded="true" aria-controls="collapse14">14. Allow font-size adjustments.
                </a>
              </h4>
            </div>
            <div id="collapse14" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading14">
              <div class="panel-body">
        		<p>You don't want to make visitors squint while reading content on your page. You don't want them to pinch and zoom either. A good solution is to include a widget that allows users to adjust the font-size to their preference.</p>
              </div>
            </div>
          </div>                                             
                     
          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading15">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse15" aria-expanded="true" aria-controls="collapse15">15. Don't use Flash.
                </a>
              </h4>
            </div>
            <div id="collapse15" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading15">
              <div class="panel-body">
        		<p>Flash is a multimedia and software platform used to create vector graphics, animation, games and rich Internet applications that can be viewed with a Adobe Flash Player. Unfortunately, not all mobile devices by default has the plugin to play Flash files.</p>
                <p>If you want to create special effects, use HTML5 instead. If you want to try do your own animation for your website minus the programming and software downloads, you can try <a href="https://animatron.com/" target="_blank">animatron.com</a>.</p>
              </div>
            </div>
          </div>    
              
          
          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading17">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse17" aria-expanded="true" aria-controls="collapse17">16. Simplify the Content.</a></h4>
            </div>
            <div id="collapse17" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading17">
              <div class="panel-body">
        		<p>Regardless of how large your phone screen is, it is still less than a quarter of the size of the average laptop screen. Long content will tire your readers from scrolling and reading from a smaller screen.</p>
                <p>Remember that mobile users are usually reading while-on-the-go. It will be a big help for them for you to reduce clutter and amount of content on your mobile site, especially if you want to deliver a message fast.</p>
              </div>
            </div>
          </div>                        

          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading18">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse18" aria-expanded="true" aria-controls="collapse18">17. Incorporate Your Brand.</a></h4>
            </div>
            <div id="collapse18" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading18">
              <div class="panel-body">
        		<p>Just because your content is shorter doesn't mean you your website becomes unidentifiable. Retain your branding by incorporating same fonts, colour schemes and icons that would appear on your full site. Of course, never forget your logo.</p>
              </div>
            </div>
          </div>      

          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading19">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse19" aria-expanded="true" aria-controls="collapse19">18. Include Your Phone Number.</a></h4>
            </div>
            <div id="collapse19" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading19">
              <div class="panel-body">
        		<p>Prominently include your phone number on the website or use a "click to call" button. Many mobile users search on mobile with the intention of calling your business. So make it easy for them to contact you.</p>
              </div>
            </div>
          </div>      

          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading20">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse20" aria-expanded="true" aria-controls="collapse20">19. Link to Full Site.</a></h4>
            </div>
            <div id="collapse20" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading20">
              <div class="panel-body">
        		<p>If you still maintain a separate mobile website for whatever reason, including all options for accessibility is a great way to service your market. Sometimes your visitor may still need to access your full (desktop-version) website.</p>
              </div>
            </div>
          </div>      


          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading21">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse21" aria-expanded="true" aria-controls="collapse21">20. Consider studying how to make content management systems (CMS) mobile-friendly,
                </a>
              </h4>
            </div>
            <div id="collapse21" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading21">
              <div class="panel-body">
        		<p>Content Management System (CMS) is a web application designed to make it easy for non-technical users to add, edit and manage a website. Popular CMS applications are Wordpress, Drupal, and Joomla.</p>
                <p>Each CMS platform is different and has different ways to be mobile-friendly. Note that these will include technical details you may not be used to, if you are not a programmer. For a detailed documentation about this, go to <a href="http://bit.ly/1ESA5QO" target="_blank">http://bit.ly/1ESA5QO</a>.</p>
              </div>
            </div>
          </div>      

          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading22">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse22" aria-expanded="true" aria-controls="collapse22">21. Avoid perfectionism.
                </a>
              </h4>
            </div>
            <div id="collapse22" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading22">
              <div class="panel-body">
        		<p>Since mobile devices are constantly changing, do not make your site perfect for ALL available devices in one go. That will take forever to do. </p> 
                <p>Instead, research what mobile devices the majority of your users use. Then ensure the website comes out perfect for those devices. You can work on other devices later.</p>
              </div>
            </div>
          </div>      


<div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading26">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse26" aria-expanded="true" aria-controls="collapse26">22. Use analytics to define what devices to focus on.
                </a>
              </h4>
            </div>
            <div id="collapse26" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading26">
              <div class="panel-body">
        		<p>Consulting analytics is a great way to  gather data of what mobile devices were used to access your website. This information can help to further improve your website. Ensure you have your analytics installed on your website when you launch.</p>
                <p>Analytics can also provide you the following information you can use for your marketing needs:</p>
                <ol>
                	<li>How many visitors are coming to your website</li>
                    <li>Which page is most visited by your visitors</li>
                    <li>How many people left you  messages via your website's contact form</li>
                    <li>and more!</li>
                </ol>
              </div>
            </div>
