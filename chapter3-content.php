          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="headingThree">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapseThree" aria-expanded="true" aria-controls="collapseThree">3. Study your competitors.
                </a>
              </h4>
            </div>
            <div id="collapseThree" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingThree">
              <div class="panel-body">
        		<p>Monitoring your competitors on an on-going basis will get you to observe their behavior and foresee their next step. This helps your business gain some competitive intelligence, which can help you plan your own strategies to win and keep more customers.</p>
                <p>At times, studying your competitor's websites can also give insight on improvements you can integrate to your own website.</p>
              </div>
            </div>
          </div>
          
           <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading4">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse4" aria-expanded="true" aria-controls="collapseTwo">4. Know your capabilities and limits.
                </a>
              </h4>
            </div>
            <div id="collapse4" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading4">
              <div class="panel-body">
        		<p>This helps you set a reasonable and realistic project goal.</p>
                <p>If you know how to plan and study the mobile behaviour of your users, that would be great.</p>
                <p>If you know how to design your website for mobile, that would help in giving instructions to your developer.</p>
                <p>If you are having a hard time planning your mobile website then it would be best to rely on experts for help.</p>
              </div>
            </div>
          </div>
          
           <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading5">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse5" aria-expanded="true" aria-controls="collapse5">5. Do a study on your users.
                </a>
              </h4>
            </div>
            <div id="collapse5" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading5">
              <div class="panel-body">
        		<p>If you can, invest in market research, usability testing, and/or hire an experienced UX/UI designer.</p>
                <p>UX/UI designers are primarily concerned how a website is laid out and they ensure that information about the product or service logically flows from one step to the next. By identifying user behaviours, UX/UI designers are able to pinpoint the web visitors' verbal and non-verbal stumbling blocks that they can refine and use to provide the best user experience.</p>
                <p>Remember, always keep your users' workflow in mind and not the other way around.</p>
              </div>
            </div>
          </div>
          
          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading6">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse6" aria-expanded="true" aria-controls="collapse6">6. Plan the scope of your website for mobile.
                </a>
              </h4>
            </div>
            <div id="collapse6" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading6">
              <div class="panel-body">
        		<p>Decide how many pages and what content you will be creating. Stick to it unless a change is necessary. Communicate this plan to stakeholders and project memebers in advance.</p>
                <p>This will help you put more focus on emphasis for your website's key messages. Do put into consideration that a mobile screen is smaller than  a laptop screen and users want to find information fast. </p>
                <p>Remember to keep focus on users' needs, and not on stakeholders' request.</p>
              </div>
            </div>
          </div>
          
          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading7">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse7" aria-expanded="true" aria-controls="collapse7">7. Avoid building separate mobile sites.
                </a>
              </h4>
            </div>
            <div id="collapse7" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading7">
              <div class="panel-body">
        		<p>In the long run, maintaining two websites will be more costly and time-consuming to maintain.</p>
                <p>Rather, build something which works in many devices. This will help you focus more on resources and budget for content and marketing rather than on maintenance.</p>
              </div>
            </div>
          </div>                                          

          <div class="panel panel-default">
            <div class="panel-heading" role="tab" id="heading8">
              <h4 class="panel-title">
                <a data-toggle="collapse" data-parent="#accordion" href="#collapse8" aria-expanded="true" aria-controls="collapse8">8. Make your website responsive (recommended).
                </a>
              </h4>
            </div>
            <div id="collapse8" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading8">
              <div class="panel-body">
        		<p><u>Responsive designed websites (RWD)</u> has become the leading solution to ensure good experience for your visitors when they're visiting a website via mobile devices.</p>
              	<p>Examples of responsive websites are the following:</p>
