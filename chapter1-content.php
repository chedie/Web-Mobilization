        <section class="row">
          <article class="col-xs-12"><h3>Time: <a href="http://time.com/" target="_blank">time.com</a></h3></article>
          <article class="col-md-6">
              <p><a href="http://time.com/" target="_blank"><img src="images/time-website.png"/></a></p>
              <p>Time wanted to deliver a seamless user experience across all devices.
              So they decided Responsive Web Design (RWD) was the best way to go for their growing mobile audience. In the end, the results were amazing.</p>
          </article>
          <div class="col-md-6">  
            <p><strong>Results:</strong></p>
            <ul>
                <li>25% increase in mobile and tablet traffic </li>
