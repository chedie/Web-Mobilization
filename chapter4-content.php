          	<p>&lt;picture&gt; is used in HTML5. The &lt;picture&gt; 
          	  
          	  element is a solution for providing multiple versions of an image based on different characters (e.g. device size, device solution, orientation, etc.). </p>
          	  <p>For more information about this element, visit <a href="http://bit.ly/10Ne6d8" target="_blank">http://bit.ly/10Ne6d8</a></p>
          </div>
        </div>
