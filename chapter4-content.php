            <a data-toggle="collapse" data-parent="#accordion" href="#collapseFour" aria-expanded="true" aria-controls="collapseFour">
              4. DO NOT block crawling any page assets
            </a>
          </h4>
        </div>
        <div id="collapseFour" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFour">
          <div class="panel-body">
          	<p>
          	  <p>Page assets are inclusive of  CSS files, JavaScipt codes, and images. These files must not be blocked from any Googlebots using robots.txt or other methods.</p>
			<p>Robots.txt is a file in your web server that gives instructions about your site to web robots. Have Googlebot gain access to these external files to help Google algorithms define if your website is mobile-friendly.</p></p>
          </div>
        </div>
      </div>
      
