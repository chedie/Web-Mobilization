                <a data-toggle="collapse" data-parent="#accordion" href="#collapse23" aria-expanded="true" aria-controls="collapse14">23. Prepare for a test drive before launch.
                </a>
              </h4>
            </div>
            <div id="collapse23" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading23">
              <div class="panel-body">
