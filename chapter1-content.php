        	<article class="col-md-6">
            <p><strong>Mobile optimization</strong> is a website designed and formatted to be read and navigated easily on small screens of mobile devices.</p>
            <p>Usual features of a mobile optimized websites are:</p>
                        <ul>
                            <li>They're smaller</li>
                            <li>Has less detailed text blocks and focuses more on key points</li>
                            <li>Has larger navigation buttons</li>
                            <li>Fewer graphics</li>
                            <li>Includes phone number that starts a call when clicked on</li>
                        </ul>
 
