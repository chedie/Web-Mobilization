                <li>15% increase in unique homepage visits</li>
                <li>7.5% increase in time spent on the homepage</li>
                <li>26% decrease in mobile bounce rates. Bounce rates represent percentage of visitors who enter the site and &quot;bounce&quot; (leave the site) rather than continue viewing other pages within the same site. </li>
            </ul>
          </div>
