          <h4 class="panel-title">
            <a data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
              1. Use Responsive Website Design (RWD) for your website.
            </a>
          </h4>
        </div>
        <div id="collapseOne" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingOne">
          <div class="panel-body">
          	<p>Google loves responsive web design for the following reasons:</p>
            <ol>
            	<li>Users find it easier to share and link content using only one url.</li>
                <li><p>A responsive mobile optimized website enables you to maintain your back links.</p>
                 <p>A back link is any link received by a web page (i.e. directory, website, or top level domain) from another web page. Each back link equates to a "vote" to your website, which Google uses to measure quality of your content.</p> 
                 <p>That means, if you can focus your search engine optimization on one single site and retain quality back links, all of your links will be directed to one domain, giving your responsive website a boost in Search Engine Results Page (SERP).</p></li>
                <li>Less maintenance in the long run.</li>
                <li>Requires no redirection for users to have a device-optimized view.</li>
                <li>Saves resources when Googlebot crawls your website.</li>
                
                <li><p><strong>User Experience is Enhanced.</strong> Responsive design can get you to a better and faster loading page views, provided all optimization techniques for images, stylesheets and html coding are done correctly.</p> 
                <p>A user-friendly site makes it easier for visitors to find, absorb, and pass on this content. This will make positive impact to the user's experience, which Google recognizes from a quality website.</p></li>
            </ol>
          </div>
        </div>
      </div>
    
      <div class="panel panel-default">
        <div class="panel-heading" role="tab" id="headingTwo">
          <h4 class="panel-title">
            <a data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="true" aria-controls="collapseOne">
              2. Add a meta tag to the head of the document.
            </a>
          </h4>
        </div>
        <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
          <div class="panel-body">
          	<p>Code to add at the &lt;head&gt; part of a web page document: 
          	<p><em>&lt;meta name=&quot;viewport&quot; content=&quot;width=device-width, initial-scale=1.0&quot;&gt;</em></p>
            <p>This gives instruction to the browser how to scale the page to the width of the device. Without this code, mobile browsers will show the desktop version on small screens. For users, that would entail constantly pinching and zooming on the page to find the information they need.</p>
            <p>Google prioritizes content looking better in mobile over making content accessibility hard for users.</p>
            <p><strong>NOTE: </strong>Simply placing meta tag above DOES NOT instantly make a website responsive. The website has to be coded in such a way that it will react to its window's size parameters and scale elements to defined proportions.</p>
          </div>
        </div>
      </div>
      
      <div class="panel panel-default">
        <div class="panel-heading" role="tab" id="headingThree">
