            <a data-toggle="collapse" data-parent="#accordion" href="#collapseSix" aria-expanded="true" aria-controls="collapseSix">
              6. Never have faulty redirects.
            </a>
          </h4>
        </div>
        <div id="collapseSix" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSix">
          <div class="panel-body">
          	<p>If you have separate mobile URLs, make sure you redirect mobile users on each desktop URL to the appropriate mobile URL. Otherwise, this can lead to a drop in SEO rankings.</p>
            <p>A responsive website can eliminate this problem by providing all the same content as found on the desktop web version.</p>
